package java_decision_statment;

public class switch_statement_002 {

	public static void main(String[] args) {
		
String	s =	"xy";

switch (s) {
case "cd":
	System.out.println("value is cd");
	break;
case "ef":
	System.out.println("value is ef");
	break;
case "ab":
	System.out.println("value is ef");
	break;


default:
	System.out.println("value is invalid");
	break;
}

		


	}

}
