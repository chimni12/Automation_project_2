package java_decision_statment;

public class switch_ststement {

	public static void main(String[] args) {
		
		int i = 700;
		// switch statement
		
		switch (i) {
		case 100:
			System.out.println("number is 100");
			break;
			
		case 200:
			System.out.println("number is 200");
			break;
			
		case 300:
			System.out.println("number is 300");
			break;
			
		case 400:
			System.out.println("number is 400");
			break;
			
		case 500:
			System.out.println("number is 500");
			break;
			
			default:
				System.out.println("number is  invalid");
				break;
		
		}

	}

}
