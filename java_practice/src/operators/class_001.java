package operators;

public class class_001 {

	public static void main(String[] args) {

		int  i = 70;
		int  s = 30;
		
		// 1st way
	    // Addition on two variables
		System.out.println(i+s);
		
		// subtract
		System.out.println(i-s);
		
		// multiplication 
		System.out.println(i*s);
		
		// division-return
		System.out.println(i/s);
		
		// modulus-return remaindor
		System.out.println(i%s);
		//increment
		
		//System.out.println(++s);
		
		//decrement - --
		System.out.println(--i);
		
		System.out.println(--s);
		
		
	
		//increment - ++
		System.out.println(++i);
		
	}

}
